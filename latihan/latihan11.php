<?php
// Variabel Super Global : bersifat global dan bawaan dari php, berfungsi untuk menyelesaikan task yang kita sudah buat

$_SERVER;
// array aso yang menyediakan info tentang request server

$_GET;
// array aso nilai dan string visible

$_POST;
// array aso nilai dan string invisible

$_COOKIE;
// menyimpan data dari pengguna, cart pada toko online

$_SESSION;
// menyimpan data dari pengguna untuk login, default time 24 minutes

$_REQUEST;
// untuk menyimpan gabunaan nilai dari variabel $_GET, $_POST, dan $_COOKIE

$_FILES;
// untuk mendownload atau upload file menggunakan metode POST atau PUT

$_ENV;
// biasanya untuk database

$_GLOBALS;
// mendefinisikan semua variabel menjadi global



?>
